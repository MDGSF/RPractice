# 优先级  符号
# 1        ^   乘方
# 2        %%  整除求余
# 3        %/% 整除
# 4        *   
# 5        /   
# 6        +   
# 7        -   

print(1 + 2 * 3) # 7
print((1 + 2) * 3) # 9
print(3 / 4) # 0.75
print(3.4 - 1.2) # 2.2
print(1 - 4 * 0.5^3) # 0.5
print(8 / 3 %% 2) # 8
print(8 / 4 %% 2) # Inf
print(3 %% 2 ^ 2) # 3
print(10 / 3 %/% 2) # 10

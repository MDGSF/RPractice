# 创建因子

# 将无序因子转换为数值
a <- c("金融", "地产", "医药", "医药", "金融", "医药") # 向量 a
f1 <- factor(a) # 将向量 a 编码为因子
f1
as.numeric(f1) # 将因子转换为数值

# 将无序因子转换为有序因子或数值
b <- c("很好", "好", "一般", "差", "很差") # 向量 b
f2 <- factor(b, ordered=TRUE, levels=c("很好", "好", "一般", "差", "很差")) # 将向量 b 编码为有序因子
as.numeric(f2) # 将因子编码为数值
